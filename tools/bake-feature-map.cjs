#!/usr/bin/env node
// Bake the PUBLISHED feature-map layer back into the code catalog.
//
// Housekeeping for the feature map: folds the published overrides
// (zane_feature_map_published) into src/feature-map-db.js so the code catalog
// (the always-available base / offline fallback) stays current and the DB delta
// layer can be reset. Run manually via .github/workflows/bake-feature-map.yml.
//
// Two modes:
//   node tools/bake-feature-map.cjs           regenerate catalog + bump SW cache
//   node tools/bake-feature-map.cjs --clear   empty both DB layers (after push)
//
// The regenerate mode refuses to run while the draft has unpublished changes
// (draft != published): baking then would strand that work. Publish or discard
// first. Requires SUPABASE_SERVICE_ROLE_KEY (+ optional SUPABASE_URL).

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const root = path.join(__dirname, '..');
const SUPABASE_URL = process.env.SUPABASE_URL || 'https://ebbuvdzgstrhrcsbrlez.supabase.co';

const HEADER = `/* Feature Map: the versioned master catalog of what the app can do.
   This is the single source of truth (base layer) for the feature map. Everyone
   renders it, then layers the live published curation on top; the public page
   uses it as the offline fallback. Keep it current as the app gains features:
   add or edit a card below, one object per capability.

   This file is also regenerated by the bake workflow
   (tools/bake-feature-map.cjs), which folds the published DB layer back in. Hand
   edits stay welcome; the serializer keeps the same shape. Plain JS, loaded via
   a normal <script> in index.html and listed in sw.js ASSETS (not the JSX loader).

   Card shape: { id, cat, role, name, summary, actions:[...], hidden? }
   - id:     stable slug, never reuse or change once shipped (admin overrides key on it)
   - role:   'user' (lifter) | 'coach' | 'both'
   - hidden: optional; true = kept in the catalog but not shown (curated out)
*/
`;

// ── DB helpers (REST via the service_role key) ──────────────────────────────
function headers() {
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!key) { console.error('SUPABASE_SERVICE_ROLE_KEY is required'); process.exit(1); }
  return { apikey: key, Authorization: 'Bearer ' + key, 'Content-Type': 'application/json' };
}
async function rest(pathq, opts = {}) {
  const res = await fetch(SUPABASE_URL + '/rest/v1/' + pathq, { headers: headers(), ...opts });
  if (!res.ok) throw new Error(`REST ${pathq} -> ${res.status} ${await res.text()}`);
  return res;
}
const selectAll = (table) => rest(table + '?select=*').then(r => r.json());
const clearTable = (table) => rest(table + '?card_id=neq.__none__', { method: 'DELETE' });

// ── Pure logic (unit-tested) ────────────────────────────────────────────────

// Order-independent equality of two override row-sets (draft vs published).
function rowKey(r) {
  return JSON.stringify([
    r.card_id, !!r.hidden, !!r.is_custom,
    r.cat ?? null, r.name ?? null, r.role ?? null,
    r.summary ?? null, r.actions ?? null, r.sort ?? null,
  ]);
}
function sameRowsets(a, b) {
  if (a.length !== b.length) return false;
  const sa = a.map(rowKey).sort(), sb = b.map(rowKey).sort();
  return sa.every((x, i) => x === sb[i]);
}

// Load the catalog by evaluating the plain-JS file with a stub window.
function loadCatalog() {
  const src = fs.readFileSync(path.join(root, 'src/feature-map-db.js'), 'utf8');
  const sandbox = { window: {} };
  vm.runInNewContext(src, sandbox);
  return sandbox.window.FEATURE_MAP;
}

// Fold published overrides into the catalog cards (same merge as the app/public
// page), producing catalog-shaped cards in effective order. Sort is dropped:
// order becomes implicit in the array position within each category.
function fold(catalog, rows) {
  const ov = {}; rows.forEach(r => { ov[r.card_id] = r; });
  const out = [], per = {};
  for (const c of catalog.cards) {
    const idx = (per[c.cat] = per[c.cat] == null ? 0 : per[c.cat] + 1);
    const o = ov[c.id];
    out.push({
      id: c.id, cat: (o && o.cat) || c.cat, role: (o && o.role) || c.role,
      name: (o && o.name != null) ? o.name : c.name,
      summary: (o && o.summary != null) ? o.summary : c.summary,
      actions: (o && o.actions != null) ? o.actions : (c.actions || []),
      hidden: o ? !!o.hidden : !!c.hidden,
      _sort: (o && o.sort != null) ? o.sort : idx,
    });
  }
  for (const id in ov) {
    const o = ov[id]; if (!o.is_custom) continue;
    out.push({
      id, cat: o.cat, role: o.role || 'user', name: o.name || '',
      summary: o.summary || '', actions: o.actions || [], hidden: !!o.hidden,
      _sort: o.sort != null ? o.sort : 9999,
    });
  }
  const byCat = {}; out.forEach(c => { (byCat[c.cat] = byCat[c.cat] || []).push(c); });
  const pick = (c) => ({ id: c.id, cat: c.cat, role: c.role, name: c.name, summary: c.summary, actions: c.actions, hidden: c.hidden });
  const cards = [];
  for (const cat of catalog.categories) {
    (byCat[cat.id] || []).sort((a, b) => (a._sort - b._sort) || String(a.name).localeCompare(b.name)).forEach(c => cards.push(pick(c)));
  }
  for (const cid of Object.keys(byCat)) {
    if (catalog.categories.some(x => x.id === cid)) continue;
    byCat[cid].forEach(c => cards.push(pick(c)));
  }
  return cards;
}

// Deterministic serialization back to the plain-JS catalog file.
function serialize(catalog) {
  const arr = (a) => '[' + (a || []).map(x => JSON.stringify(x)).join(', ') + ']';
  const cats = catalog.categories.map(c =>
    `    { id: ${JSON.stringify(c.id)}, label: ${JSON.stringify(c.label)}, icon: ${JSON.stringify(c.icon)}, blurb: ${JSON.stringify(c.blurb || '')} },`
  ).join('\n');
  const cards = catalog.cards.map(c =>
    `    { id: ${JSON.stringify(c.id)}, cat: ${JSON.stringify(c.cat)}, role: ${JSON.stringify(c.role)}, name: ${JSON.stringify(c.name)}, summary: ${JSON.stringify(c.summary)}, actions: ${arr(c.actions)}${c.hidden ? ', hidden: true' : ''} },`
  ).join('\n');
  return `${HEADER}window.FEATURE_MAP = {\n  version: ${JSON.stringify(catalog.version)},\n  categories: [\n${cats}\n  ],\n  cards: [\n${cards}\n  ],\n};\n`;
}

function bumpVersion(oldVersion) {
  const m = String(oldVersion || 'v1').match(/^v(\d+)/);
  const next = m ? parseInt(m[1], 10) + 1 : 2;
  return `v${next} (${new Date().toISOString().slice(0, 10)})`;
}

// ── Entry point ─────────────────────────────────────────────────────────────
async function main() {
  if (process.argv.includes('--clear')) {
    await clearTable('zane_feature_map_published');
    await clearTable('zane_feature_map');
    console.log('Cleared the draft + published feature-map layers.');
    return;
  }

  const [draft, published] = await Promise.all([
    selectAll('zane_feature_map'),
    selectAll('zane_feature_map_published'),
  ]);

  if (!sameRowsets(draft, published)) {
    console.error('Refusing to bake: the draft has unpublished changes.');
    console.error('Publish or discard them in the app first, then re-run this workflow.');
    process.exit(2);
  }
  if (published.length === 0) {
    console.log('Nothing to bake: the published layer is empty (the catalog already reflects it).');
    return;
  }

  const catalog = loadCatalog();
  const version = bumpVersion(catalog.version);
  const cards = fold(catalog, published);
  fs.writeFileSync(path.join(root, 'src/feature-map-db.js'), serialize({ version, categories: catalog.categories, cards }));

  const swPath = path.join(root, 'sw.js');
  const sw = fs.readFileSync(swPath, 'utf8');
  const bumped = sw.replace(/const CACHE = 'zane-v(\d+)\.(\d+)';/, (_, maj, min) => `const CACHE = 'zane-v${maj}.${parseInt(min, 10) + 1}';`);
  if (bumped === sw) throw new Error('Could not find the CACHE version line in sw.js to bump.');
  fs.writeFileSync(swPath, bumped);

  console.log(`Baked ${cards.length} cards into src/feature-map-db.js (version ${version}) and bumped the SW cache.`);
}

if (require.main === module) main().catch(e => { console.error(e.message || e); process.exit(1); });
module.exports = { rowKey, sameRowsets, loadCatalog, fold, serialize, bumpVersion };
